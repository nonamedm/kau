<script>
jQuery(function($) {

    $(".faq_list ul li button").click(function() {
        $(this).addClass('on').closest('li').siblings().find('button').removeClass('on');
        const currentId = $(this).data('menu_id');

        $(this).addClass('on').siblings().removeClass('on');
        $("#"+currentId).show().siblings().hide();
    });

    $('li').click(function() {
        $(this).find('.question').toggleClass('on').closest('li').siblings().find('.question').removeClass('on');
        $(this).find('.answer').slideToggle().closest('li').siblings().find('.answer').slideUp();
    });
});

</script>

